class Phone:

    def __init__(self, brand, model, number, color):
        self.brand = brand
        self.model = model
        self.number = number
        self.color = color

    def ring(self):
        print("ring ring ring!")
        print(f"Incoming call on: {self.number}")
        print(f"Brand: {self.brand}, Model: {self.model}, Color: {self.color}")


    def call(self, other_number):
        print(f"Calling {other_number} from my {self.brand} phone.")

    def play_game(self, game_name):
        print(f"Playing {game_name} on my {self.model}.")


